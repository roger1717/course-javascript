

// AND (&&)  devuelve true si se cumplen todas las condiciones
// OR (||)  devuelve true si alguna condicion se cumple
// NOT (!)  devuelve true si es diferente de la condicion

console.log(true && true);
console.log(true && false);
console.log(false && true);

console.log(true || true);
console.log(true || false);
console.log(false || false); 

console.log(!true);
console.log(!false); 
